import { createContext, useContext, useEffect, useState } from 'react'
import { z } from 'zod/v4'
import { C1Component, ThemeProvider } from '@thesysai/genui-sdk'
import '@crayonai/react-ui/styles/index.css'
import { api, fmtTs, sourceOf } from './api'
import { Button, ImpactCell, Skeleton, SourceBadge, StatusPill } from './primitives'

// The four custom components C1 composes the workspace from. Each prefers
// the LIVE case (App polls every 2s) over its generated props, so the
// workspace stays reactive — a draft attaching or a status morph shows up
// without re-calling C1.

const WorkspaceCtx = createContext(null)
export const useWorkspace = () => useContext(WorkspaceCtx)

export function ImpactBrief(props) {
  // Live case data wins outright when present — C1's job is composition;
  // props only carry data when there is no live case to read from.
  const { liveCase } = useWorkspace()
  const c = liveCase || {}
  const live = !!liveCase
  const impact = live ? c.impact_level : props.impactLevel
  const summary = live ? c.summary : props.summary
  const effective = live ? c.effective_date : props.effectiveDate
  const url = live ? c.url : props.url
  if (live && !c.impact_level) {
    if (c.status === 'CLOSED') {
      // auto-closed as not relevant — no brief exists; show the agent's reasoning
      const closeNote = (c.timeline || []).filter((e) => e.action === 'closed').pop()?.note
      return (
        <div className="border-b border-hairline px-4 py-3">
          <div className="mb-2 flex items-center gap-2">
            <SourceBadge source={sourceOf(c)} />
            <span className="rounded bg-stone-100 px-1.5 py-0.5 text-[11px] font-semibold text-stone-600">
              NOT RELEVANT
            </span>
          </div>
          <p className="text-[13px] leading-relaxed text-ink-2">
            {closeNote || 'Classified not relevant to our charter — closed with no action.'}
          </p>
          {c.url && (
            <a
              href={c.url}
              target="_blank"
              rel="noreferrer"
              className="mt-1.5 inline-block text-[12px] font-medium text-blue-600 hover:underline"
            >
              View source document
            </a>
          )}
        </div>
      )
    }
    return (
      <div className="border-b border-hairline px-4 py-3">
        <div className="mb-2 flex items-center gap-2">
          <SourceBadge source={sourceOf(c)} />
          <span className="text-[11px] text-ink-3">classifying impact…</span>
        </div>
        <div className="space-y-2">
          <Skeleton className="h-3 w-full" />
          <Skeleton className="h-3 w-4/5" />
        </div>
      </div>
    )
  }
  return (
    <div className="border-b border-hairline px-4 py-3">
      <div className="mb-2 flex items-center gap-2">
        <SourceBadge source={sourceOf(c) || props.source} />
        <ImpactCell level={impact} />
        {effective && (
          <span className="tnum text-[11px] text-ink-3">effective {effective}</span>
        )}
      </div>
      <p className="text-[13px] leading-relaxed text-ink-2">{summary}</p>
      {url && (
        <a
          href={url}
          target="_blank"
          rel="noreferrer"
          className="mt-1.5 inline-block text-[12px] font-medium text-blue-600 hover:underline"
        >
          View source document
        </a>
      )}
    </div>
  )
}

export function DraftMemo(props) {
  const { liveCase } = useWorkspace()
  const draft = liveCase?.draft ?? props
  const pending = draft.pending
  if (pending && liveCase?.status === 'CLOSED') {
    return (
      <div className="border-b border-hairline px-4 py-3 text-[12px] text-ink-3">
        No notification — case closed with no action required.
      </div>
    )
  }
  return (
    <div className="border-b border-hairline px-4 py-3">
      <div className="mb-2 flex items-center gap-2">
        <span className="rounded bg-stone-100 px-1.5 py-0.5 text-[10px] font-semibold uppercase tracking-wide text-ink-2">
          Draft — generated by Canary
        </span>
        {pending && <span className="text-[11px] text-ink-3">drafting…</span>}
      </div>
      {pending ? (
        <div className="space-y-2">
          <Skeleton className="h-3 w-2/3" />
          <Skeleton className="h-3 w-full" />
          <Skeleton className="h-3 w-full" />
          <Skeleton className="h-3 w-4/5" />
        </div>
      ) : (
        <div className="rounded-md border border-hairline bg-rail px-3 py-2.5">
          <div className="border-b border-hairline pb-2 text-[12px]">
            <div>
              <span className="text-ink-3">To: </span>
              <span className="font-medium">{(draft.to || []).join(', ')}</span>
            </div>
            <div>
              <span className="text-ink-3">Subject: </span>
              <span className="font-medium">{draft.subject}</span>
            </div>
          </div>
          <p className="whitespace-pre-line pt-2 text-[13px] leading-relaxed text-ink-2">
            {draft.body}
          </p>
        </div>
      )}
    </div>
  )
}

export function RecommendedActions(props) {
  // C1-generated analysis — the one component whose content the model
  // derives rather than copies; intentionally NOT live-overridden.
  const items = props.items || []
  if (!items.length) return null
  return (
    <div className="border-b border-hairline px-4 py-3">
      <div className="mb-2 flex items-center gap-2">
        <span className="text-[10px] font-semibold tracking-wide text-ink-3 uppercase">
          Close-out checklist
        </span>
        <span className="rounded bg-stone-100 px-1.5 py-0.5 text-[10px] text-ink-3">
          generated by C1
        </span>
      </div>
      <div className="space-y-2">
        {items.map((it, i) => {
          const owner = it.owner ?? it.unit
          const deadline = it.deadline ?? it.timeframe
          return (
            <div key={i} className="flex items-start gap-2 text-[12.5px]">
              <span className="mt-0.5 h-3.5 w-3.5 shrink-0 rounded-sm border border-hairline" />
              <span className="min-w-0 flex-1">
                <span className="text-ink">{it.action}</span>
                <span className="mt-0.5 flex items-center gap-1.5">
                  {owner && (
                    <span className="rounded bg-stone-100 px-1.5 py-0.5 text-[11px] whitespace-nowrap text-ink-2">
                      {owner}
                    </span>
                  )}
                  {deadline && <span className="tnum text-[11px] text-ink-3">{deadline}</span>}
                </span>
              </span>
            </div>
          )
        })}
      </div>
    </div>
  )
}

// lifecycle tracker — the pizza-tracker strip at the top of every workspace
const STAGES = ['Detected', 'Triaged', 'Notified', 'In progress', 'Closed']
const STAGE_OF = { NEW: 0, TRIAGED: 1, NOTIFIED: 2, IN_PROGRESS: 3, CLOSED: 4 }

export function LifecycleTracker({ status, impact }) {
  const cur = STAGE_OF[status] ?? 0
  const held = status === 'TRIAGED' && impact === 'HIGH'
  const doneColor = status === 'CLOSED' ? 'bg-green-600' : 'bg-blue-600'
  return (
    <div className="border-b border-hairline px-4 pt-3 pb-2">
      <div className="flex items-center">
        {STAGES.map((s, i) => (
          <span key={s} className="contents">
            {i > 0 && (
              <span className={`mx-1 h-px flex-1 ${i <= cur ? doneColor : 'bg-hairline'}`} />
            )}
            <span
              className={`h-2.5 w-2.5 shrink-0 rounded-full transition-colors duration-200 ${
                i < cur || (i === cur && status === 'CLOSED')
                  ? doneColor
                  : i === cur
                    ? `border-2 bg-white ${held ? 'border-red-700' : 'border-blue-600'}`
                    : 'border border-hairline bg-white'
              }`}
            />
          </span>
        ))}
      </div>
      <div className="mt-1 flex justify-between">
        {STAGES.map((s, i) => (
          <span
            key={s}
            className={`text-[9px] tracking-wide uppercase ${
              i === cur && status !== 'CLOSED'
                ? held
                  ? 'font-semibold text-red-700'
                  : 'font-semibold text-blue-700'
                : i <= cur
                  ? 'text-ink-2'
                  : 'text-ink-3'
            }`}
          >
            {i === 1 && held ? 'Awaiting review' : s}
          </span>
        ))}
      </div>
    </div>
  )
}

function Modal({ title, onClose, children }) {
  return (
    <div
      id="canary-modal"
      className="fixed inset-0 z-50 flex items-center justify-center bg-stone-900/25"
      onClick={onClose}
    >
      <div
        className="w-[440px] rounded-lg border border-hairline bg-white p-4 shadow-lg"
        onClick={(e) => e.stopPropagation()}
        onKeyDown={(e) => e.key === 'Escape' && onClose()}
      >
        <div className="mb-3 text-[14px] font-semibold">{title}</div>
        {children}
      </div>
    </div>
  )
}

