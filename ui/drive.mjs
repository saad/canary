import { chromium } from 'playwright'
const browser = await chromium.launch({ channel: 'chrome', headless: true })
const page = await (await browser.newContext({ viewport: { width: 1440, height: 860 } })).newPage()
await page.goto('http://127.0.0.1:8400/', { waitUntil: 'domcontentloaded' })
await page.waitForSelector('main tbody tr', { timeout: 15000 })
await page.locator('main tbody tr').first().click()
await page.waitForSelector('text=workspace generated by Thesys C1', { timeout: 30000 })
const h = page.locator('aside button.absolute[title="Expand to full width"]')
const box = await h.boundingBox()
console.log('handle box:', box)
// is it actually on top (clickable at its center)?
const hit = await page.evaluate(({ x, y }) => {
  const el = document.elementFromPoint(x, y)
  return el?.tagName + (el?.title ? ':' + el.title : '')
}, { x: box.x + box.width / 2, y: box.y + box.height / 2 })
console.log('element at handle center:', hit)
const scrollable = await page.evaluate(() => document.documentElement.scrollHeight > window.innerHeight + 4)
console.log('page scroll still contained:', !scrollable)
await page.screenshot({ path: '/tmp/i1-handle.png', clip: { x: 920, y: 380, width: 160, height: 120 } })
await browser.close()
